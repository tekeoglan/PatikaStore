package com.tekeoglan.patikastore;

class PhoneProperties {
    int storage;
    double display;
    int cameraResolution;
    double battery;
    int memory;
    String color;

    PhoneProperties(int storage, double display, int cameraResolution, double battery, int memory, String color) {
        this.storage = storage;
        this.display = display;
        this.cameraResolution = cameraResolution;
        this.battery = battery;
        this.memory = memory;
        this.color = color;
    }
}
